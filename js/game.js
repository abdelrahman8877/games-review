export class Game {
    constructor(id, title, thumbnail, genre, platform, description, game_url, status) {
        this.id = id;
        this.title = title;
        this.thumbnail = thumbnail;
        this.genre = genre;
        this.platform = platform;
        this.description = description;
        this.game_url = game_url;
        this.status = status;
    }
}
